    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
        try {
            Disciplina disciplina = new Disciplina(discField.getText(), Integer.parseInt(horasField.getText())+0000,
                Integer.parseInt(creditosField.getText()), Integer.parseInt(codigoProfField.getText()));
            DisciplinaController control = new DisciplinaController();
            if(control.save(disciplina)){
                JOptionPane.showMessageDialog(null, "Disciplina Registada");dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Erro");dispose();
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(discRegisterForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_atualizarActionPerformed

