    private void registarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registarActionPerformed
        try {
            Estudante estudante = new Estudante(Integer.parseInt(anoIngressoField.getText()), nomeField.getText(),
                (String)generoBox.getSelectedItem().toString().substring(0,1));
            EstudanteController control = new EstudanteController();
            if(control.save(estudante)){
                JOptionPane.showMessageDialog(null, "Estudante Registado");
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao registar");dispose();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(estRegisterForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_registarActionPerformed

