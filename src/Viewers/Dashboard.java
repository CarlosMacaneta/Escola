    public void fillTable(){
        try {
            DefaultTableModel modelo = (DefaultTableModel)estTable.getModel();
            modelo.setNumRows(0);
            EstudanteController estControl = new EstudanteController();
            for(int i = 0; i < estControl.showAll().size(); i++){
                String data[] = {String.valueOf(estControl.showAll().get(i).getCodigo()), estControl.showAll().get(i).getNome(),
                estControl.showAll().get(i).getGenero(), String.valueOf(estControl.showAll().get(i).getAnoIngresso())};
                DefaultTableModel model = (DefaultTableModel)estTable.getModel();
                model.addRow(data);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
